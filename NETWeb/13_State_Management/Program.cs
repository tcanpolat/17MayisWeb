namespace _13_State_Management
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();

            // Bellek içi daðýtýlmýþ önbellek servisi ekler. Önbelleðe alýnan öðeler uygulamanýn çalýþtýðý sunucudaki uygulama örneði tarafýndan depolanýr.
            builder.Services.AddDistributedMemoryCache();

            // Session Yapýlandýrma
            builder.Services.AddSession(opt =>
            {
                opt.IdleTimeout = TimeSpan.FromMinutes(30); // Oturum geçerlilik süresi
                opt.Cookie.HttpOnly = true;
                opt.Cookie.IsEssential = true; // Cookie'nin temel iþlevsellik için gereklidir.
            });

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
                // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
                app.UseHsts();
            }

            app.UseHttpsRedirection();
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();
            app.UseSession();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}");

            app.Run();
        }
    }
}
